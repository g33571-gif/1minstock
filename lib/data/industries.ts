// 종목 코드로 업종명 가져오기
export function getIndustryName(code: string): string {
  return STOCK_INDUSTRY_MAP[code] || '';
}

// 종목이 매핑되어 있는지 확인
export function hasIndustryMapping(code: string): boolean {
  return code in STOCK_INDUSTRY_MAP;
}

// PER 비교 (저평가/적정/고평가 판단)
export function comparePer(per: number, code: string): ValuationCompare | null {
  const industryName = getIndustryName(code);
  if (!industryName) return null;
  
  const avg = INDUSTRY_AVG[industryName];
  if (!avg || per <= 0) return null;
  
  const diffPercent = Math.round(((per - avg.per) / avg.per) * 100);
  let label: '저평가' | '적정' | '고평가';
  let color: 'blue' | 'gray' | 'red';
  
  if (diffPercent <= -20) {
    label = '저평가';
    color = 'blue';
  } else if (diffPercent >= 30) {
    label = '고평가';
    color = 'red';
  } else {
    label = '적정';
    color = 'gray';
  }
  
  return {
    industryName,
    myValue: per,
    industryAvg: avg.per,
    diffPercent,
    label,
    color,
  };
}

